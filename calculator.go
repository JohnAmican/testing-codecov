package calculator

// Add adds x and y
func Add(x, y int) int {
	return x + y
}

// Subtract subtracts y from x
func Subtract(x, y int) int {
	return x - y
}

// Multiply multiple x and y
func Multiply(x, y int) int {
	return x * y
}

// Divide divide y from x
func Divide(x, y int) int {
	return x / y
}
